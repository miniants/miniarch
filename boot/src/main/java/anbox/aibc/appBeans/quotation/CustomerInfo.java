package anbox.aibc.appBeans.quotation;

public class CustomerInfo {
	private String personType;         //关系人类型 投保人被保人车主
	private String personClass;        //关系人类别 
	private String customerType;       //客户类型 
	private String vehicleRelation;    //车辆所有关系
	private String name;               //姓名
	private String sex;                //性别
	private String birthday;           //生日
	private String certType;           //证件类型
	private String certNo;             //证件号码
	private String mobile;             //手机号
	private String tel;                //电话
	private String email;              //邮箱
	private String postCode;           //寄送邮编
	private String postAddr;           //寄送地址
	public String getPersonType() {
		return personType;
	}
	public void setPersonType(String personType) {
		this.personType = personType;
	}
	public String getPersonClass() {
		return personClass;
	}
	public void setPersonClass(String personClass) {
		this.personClass = personClass;
	}
	public String getCustomerType() {
		return customerType;
	}
	public void setCustomerType(String customerType) {
		this.customerType = customerType;
	}
	public String getVehicleRelation() {
		return vehicleRelation;
	}
	public void setVehicleRelation(String vehicleRelation) {
		this.vehicleRelation = vehicleRelation;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public String getCertType() {
		return certType;
	}
	public void setCertType(String certType) {
		this.certType = certType;
	}
	public String getCertNo() {
		return certNo;
	}
	public void setCertNo(String certNo) {
		this.certNo = certNo;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPostCode() {
		return postCode;
	}
	public void setPostCode(String postCode) {
		this.postCode = postCode;
	}
	public String getPostAddr() {
		return postAddr;
	}
	public void setPostAddr(String postAddr) {
		this.postAddr = postAddr;
	}
	
}
